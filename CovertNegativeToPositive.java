package week1.day1.assignments;

public class CovertNegativeToPositive {

	public static void main(String[] args) {
		long input1 =-1010;
		if(input1>0) {
		System.out.println(input1+" is already positive number");
	}else
	{
		System.out.println(input1+" is converted to positive as "+input1*-1);
	}
	}
}
